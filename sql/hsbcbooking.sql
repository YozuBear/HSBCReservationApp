/*
Table: deskreservation
Stores desk reservation info, and on hold reservations
!!TODO: max reservation 3000
DATETIME - format: YYYY-MM-DD HH:MI:SS
rid: auto increment,unique id of reservation
desk_id: the desk that was reserved
employee: foreign key reference empid
status: reservation status: onhold/reserved
create_datetime: DATETIME when reservation is confirmed
start_datetime: DATETIME of the start point
end_datetime: DATETIME of the end point
*/
CREATE TABLE deskreservation (
	rid int NOT NULL auto_increment,
	desk_id varchar(16) NOT NULL,
    location_name varchar(64) NOT NULL,
    employee varchar(8) NOT NULL,
    status varchar(32) NOT NULL,
    create_datetime DATETIME NOT NULL,
    start_datetime DATETIME NOT NULL,
    end_datetime DATETIME NOT NULL,
    PRIMARY KEY (rid),
    FOREIGN KEY (employee) REFERENCES employee(id),
	FOREIGN KEY (desk_id) REFERENCES desk(number) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (location_name) REFERENCES location(officename) ON DELETE CASCADE ON UPDATE CASCADE
    );

/*
Table: archive
archiveid: auto increment
emp_id: foreign key reference employee.id
create_datetime: DATETIME when reservation is confirmed
start_datetime: DATETIME of reservation start point
end_datetime: DATETIME of reservation end point
archive_datetime: DATETIME when reservation is offloaded from deskreservation table; loaded to archive
*/

