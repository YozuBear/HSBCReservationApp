CREATE TABLE floorSection (
	building varchar(64) NOT NULL,
        floor int(4) NOT NULL,
        section varchar(16) NOT NULL,
        mapID varchar(64) NOT NULL,
        PRIMARY KEY (building, floor, section),
        FOREIGN KEY (building) REFERENCES location(officename) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (mapID) REFERENCES mapimage(mapkey)
    );

