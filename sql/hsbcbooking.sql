/*
Table: desk
Stores all the desk information
number: the unique desk id
location: the office name where the desk locate
floor: floor number of the desk, part of the number 
section: section of the desk, part of the number
*/
CREATE TABLE desk (
	number varchar(16) NOT NULL,
    location varchar(64) NOT NULL,
    floor int(4) NOT NULL,
    section varchar(8) NOT NULL,
    mapID varchar(64) NOT NULL,
    PRIMARY KEY (number, location),
    FOREIGN KEY (location) REFERENCES location(officename) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (mapID) REFERENCES mapimage(mapkey)
    );

